public class Treatment {
    private String kodeTreatment;
    private String namaTreatment;
    private int kuota;

    /**
     * @return the kodeTreatment
     */
    public String getKodeTreatment() {
        return kodeTreatment;
    }

    /**
     * @param kodeTreatment the kodeTreatment to set
     */
    public void setKodeTreatment(String kodeTreatment) {
        this.kodeTreatment = kodeTreatment;
    }

    /**
     * @return the namaTreatment
     */
    public String getNamaTreatment() {
        return namaTreatment;
    }

    /**
     * @param namaTreatment the namaTreatment to set
     */
    public void setNamaTreatment(String namaTreatment) {
        this.namaTreatment = namaTreatment;
    }

    /**
     * @return the kuota
     */
    public int getKuota() {
        return kuota;
    }

    /**
     * @param kuota the kuota to set
     */
    public void setKuota(int kuota) {
        this.kuota = kuota;
    }
    
}
